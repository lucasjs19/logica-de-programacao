    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
       
        int num = Integer.parseInt(txtNum.getValue().toString());
        
        int resto = num % 2;
        txtResto.setText(String.valueOf(resto));
        
        int cubo = (int) Math.pow(num, 3);
        txtCubo.setText(String.valueOf(cubo));
        
        double quadrada =  Math.sqrt(num);
        txtQuadrada.setText(String.format("%.2f", quadrada));
        
        double cubica = Math.cbrt(num);
        txtCubica.setText(String.format("%.2f", cubica));
        
        int abs = Math.abs(num);
        txtModulo.setText(String.valueOf(abs));
        
        panCalc.setVisible(true);
         
    }//GEN-LAST:event_btnCalcActionPerformed

